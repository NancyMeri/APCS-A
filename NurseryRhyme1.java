/*Use a for loop to print out all of the lyrics of Ten Little 
 * Monkeys Jumping on a Bed. You can use digits in your lyric output.
*/

public class NurseryRhyme1
{
    public static void main (String [] args){
        for (int i = 10; i > 1; i--){
            System.out.println(i + " Little monkeys jumping on a bed, " +
            "one jumped up and bumped his head,Mom called the Doctor and " +
            "the doctor said \" no more monkeys jumping on a bed.\"");
        }
        System.out.println("1 Little monkey jumping on a bed, " +
            "one jumped up and bumped his head,Mom called the Doctor and " +
            "the doctor said \" no more monkeys jumping on a bed.\"");
    }
}
